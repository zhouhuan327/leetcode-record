// @lc code=start
/**
 * @param {number} x
 * @return {boolean}
 */
var isPalindrome = function(x) {
    // 排除负数
    if(x < 0) return false
    // 排除除了0 之外的 最有一位是0的数
    if(x !== 0 && x % 10 === 0) return false

    // x的一半
    let revert = 0;
    while(x > revert) {
        // 得到x的最后一位
        revert = revert * 10 + x % 10
        // 让x去掉最后一位
        x = Math.floor(x / 10)
    }
    // 当输入为 12321 时，在 while 循环的末尾我们可以得到 x = 12，revertedNumber = 123，
    // 由于处于中位的数字不影响回文（它总是与自己相等），所以我们可以简单地将其去除。(Math.floor(revert / 10))
    return x === revert || x === Math.floor(revert / 10)
};
